import Foundation

var numbs = ["Apple", "Orange", "Grape", "Banana"]

// While Loop

var i = 0
while i < 10 {
    print(i)
    i += 1
}

// For Loop
for numb in numbs {
    print(numb)
}

for numb in 1...3 {
    print(numb % 3)
}
